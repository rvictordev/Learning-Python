# Madlibs game
# word game where you create a story by filling in blanks with random words.
adjective1 = input("Enter an Adjective (description):\n")
noun1 = input("Enter a Noun (person, place, thing):\n")
adjective2 = input("Enter an Adjective (description):\n")
verb1 = input("Enter a verb ending with 'ing':\n")
adjective3 = input("Enter an Adjective (description):\n")

print(f"Today I went to a {adjective1} zoo.")
print(f"In an exhibit, I saw a {noun1}")
print(f"{noun1} was {adjective2} and {verb1}")
print(f"I was {adjective3}!")